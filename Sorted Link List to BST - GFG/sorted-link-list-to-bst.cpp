class Solution{
  public:
    
    TNode* getBst(LNode **head, int len){
      
        if(len <=0) return NULL;
        
        TNode *left = getBst(head,len/2);
        
        TNode *bst = new TNode((*head)->data);
        
        *head=(*head)->next;
        
        bst->left = left;
        
        bst->right = getBst(head, len-len/2-1);
        
        return bst;
        
    }
    
    TNode* sortedListToBST(LNode *head) {
        
        LNode *temp = head;
        int len=0;
        while(temp!=NULL){
            len++;
            temp=temp->next;
        }
        return getBst(&head, len);
    }
};

